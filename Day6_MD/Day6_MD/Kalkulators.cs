using System;
using System.Collections.Generic;
using System.Text;

namespace Day6_MD
{
    class Kalkulators
    {
        public static double Saskaitīšana(double a, double b)
        {
            return a + b;
        }
        public static double Atņemšana(double a, double b)
        {
            return a - b;
        }
        public static double Reizināšana(double a, double b)
        {
            return a * b;
        }
        public static double Dalīšana(double a, double b)
        {
            return a / b;
        }
    }
}
